package com.akelio.marabunta.input.ant;

public class AntMemory {

	private int	m0;
	private int	m1;

	public AntMemory() {
		super();
	}

	public AntMemory(int m0, int m1) {
		super();
		this.m0 = m0;
		this.m1 = m1;
	}

	public int getM0() {
		return m0;
	}

	public void setM0(int m0) {
		this.m0 = m0;
	}

	public int getM1() {
		return m1;
	}

	public void setM1(int m1) {
		this.m1 = m1;
	}

}
